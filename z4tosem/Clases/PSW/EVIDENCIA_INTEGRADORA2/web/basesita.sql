drop database if exists VETERINARIA_BASE;
create database VETERINARIA_BASE;
use VETERINARIA_BASE;

create table PERSONA(
CURP VARCHAR(18) PRIMARY KEY NOT NULL,
NOMBRE VARCHAR(20),
APELLIDO VARCHAR(20),
EDAD INT,
CONTRASEÑA INT
);

CREATE TABLE MASCOTA(
ID_MASCOTA INT PRIMARY KEY NOT NULL,
RAZA VARCHAR(20),
NOMBRE VARCHAR(20),
EDAD INT 
);

CREATE TABLE PERSONA_TELEFONO(
CURP VARCHAR(18) PRIMARY KEY NOT NULL,
TELEFONO INT
);

CREATE TABLE PERSONA_CONTRASEÑA(
CURP VARCHAR(18) PRIMARY KEY NOT NULL,
CONTRASEÑA INT
);

CREATE TABLE MASCOTA_CURP(
CURP VARCHAR(18),
ID_MASCOTA INT,
FOREIGN KEY(ID_MASCOTA)REFERENCES MASCOTA (ID_MASCOTA)ON UPDATE CASCADE ON DELETE CASCADE,
FOREIGN KEY(CURP)REFERENCES PERSONA (CURP)ON UPDATE CASCADE ON DELETE CASCADE
);